<?php

namespace App\Http\Controllers;

use App\Models\Tag;
use App\Repository\tagRepository;
use App\Repository\tagRepositoryInterface;
use Illuminate\Http\Request;

class TagController extends Controller
{
    private tagRepositoryInterface $categoryRepository;

    public function __construct(tagRepositoryInterface $categoryRepository)
    {
        $this->tagRepository = $categoryRepository;
    }
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        return view('admin.pages.tags.index');
    }

    public function fetchTag(){
        $tags = Tag::all();
        return response()->json([
            'tags' => $tags,
        ]);
    }
    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        //
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        return $this->tagRepository->createTag($request);
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        return $this->tagRepository->editTag($id);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        return $this->tagRepository->updateTag($request,$id);
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        return $this->tagRepository->deleteTag($id);
    }
}
